package lab3b;

public class OnlineStreamingMusicPlayer {
    public void playOnlineStream(String fileName) {
        System.out.println("Playing online file: " + fileName);
    }

    public void pauseOnlineStream(String fileName) {
        System.out.println("Pausing online file: " + fileName);
    }

    public void stopOnlineStream(String fileName) {
        System.out.println("Stopping online file: " + fileName);
    }
}