package lab3b;

public class LocalFileMusicPlayer {
    public void playLocalFile(String fileName) {
        System.out.println("Playing local file: " + fileName);
    }

    public void pauseLocalFile(String fileName) {
        System.out.println("Pausing local file: " + fileName);
    }

    public void stopLocalFile(String fileName) {
        System.out.println("Stopping local file: " + fileName);
    }
}