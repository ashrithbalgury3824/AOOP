package lab3b;

 class RadioStationPlayer {
    public void playRadio(String stationName) {
        System.out.println("Playing radio: " + stationName);
    }

    public void pauseRadio(String stationName) {
        System.out.println("Pausing radio: " + stationName);
    }

    public void stopRadio(String stationName) {
        System.out.println("Stopping radio: " + stationName);
    }
}